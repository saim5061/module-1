#include <stdio.h>
int main()
{
    int saim = 77;
    float fahim = 47.97;
    char tamim = 'S';
    printf("%d", saim);
    printf("%f", fahim);
    printf("%0.2f", fahim);
    printf("%c", tamim);

    // Declare a variable
    int myNum;

    // Assign a value to the variable
    myNum = 15;
    printf("%d", myNum);
    printf("My favorite number is: %d", myNum);
    return 0;
}
